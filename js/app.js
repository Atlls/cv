'use strict';

async function inyectarPDF(){

	const iframe = await iframeLoaded('docs/Alejandro aigner (2).pdf');

	console.log('Cargado el PDF');

	const spinner = document.querySelector('#spinner');
	document.body.replaceChild(iframe,spinner);

}

function iframeLoaded(src) {

    return new Promise ( resolve => 
    {
        const iframe = document.createElement('iframe');
		iframe.setAttribute('src',src);
		iframe.setAttribute('frameborder','0');
		console.log(iframe);

        iframe.addEventListener('load',resolve(iframe));
    });
}

inyectarPDF();